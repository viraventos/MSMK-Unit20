// Una sentencia
let nombre = 'Juan';

// otra sentencia
console.log('Hola Juan');

// Las estructuras de control tambien son sentencias
switch (nombre) {
    case 'Juan': console.log('Hola Sr Juan'); break;
    case 'Pedro': console.log('Hola Pedrito'); break;
    default: console.log(`Hola ${nombre}`)
}


